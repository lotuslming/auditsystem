# 1. 安装Python依赖
pip3 install paramiko cryptography Pillow

# 2. 安装系统转换工具（推荐全部安装）
sudo apt-get update

# CUPS过滤器（最推荐，专门处理打印机格式）
sudo apt-get install cups-filters

# Ghostscript（PDF处理）
sudo apt-get install ghostscript

# ImageMagick（图像转换）
sudo apt-get install imagemagick

# 可选：CUPS开发工具
sudo apt-get install libcups2-dev
